# Її параметрами будуть цілі числа, які описують довжину та ширину такого прямокутника.

def rectangle(h, w):
     return f"{w * '*'}\n" + \
            f"*{(w - 23) * ' '}* \n" * (h-1) + \
            f"{w * '*'}"
